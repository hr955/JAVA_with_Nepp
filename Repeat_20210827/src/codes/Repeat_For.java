package codes;

public class Repeat_For {
	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}

		System.out.println("=================");

		for (int i = 20; i >= 11; i--) {
			System.out.println(i);
		}

		System.out.println("=================");

		for (int i = 0; i < 5; i++) {
			System.out.println("안녕하세요");

		}

		System.out.println("=================");

		for (int i = 3; i <= 30; i += 3) {
			System.out.println(i);
		}

		System.out.println("=================");

		for (int i = 1; i < 10; i++) {
			System.out.println("2 * " + i + " = " + 2 * i);
		}

	}

}
